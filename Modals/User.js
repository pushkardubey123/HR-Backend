const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema(
  {
    role: { type: String, enum: ["superadmin","admin","employee"], required: true },
     companyId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User", 
      default: null,
    },
    status: {
  type: String,
  enum: ["pending","approved","rejected","blocked","active","inactive"],
  default: function(){ return this.role === 'admin' ? 'pending' : 'active' }
},
    name: { type: String, required: true },
    email: { type: String, required: true, unique: true },
    phone: String,
    passwordHash: { type: String, required: true },
    gender: String,
    dob: Date,
    address: String,
    departmentId: { type: mongoose.Schema.Types.ObjectId, ref: "Department" },
    designationId: { type: mongoose.Schema.Types.ObjectId, ref: "Designation" },
    shiftId: { type: mongoose.Schema.Types.ObjectId, ref: "Shift" },
    doj: Date,
    documents: [String],
    profilePic: String,
    basicSalary: {
      type: Number,
      default: 0,
    },
    pan: { type: String },
    bankAccount: { type: String },
    emergencyContact: {
      name: String,
      phone: String,
      relation: String,
    },
    otp: String,
    otpExpires: Date,
     companyName: { type: String },
    hasUsedTrial: { type: Boolean, default: false },
  },

  { timestamps: true }
);

const userTbl = mongoose.model("User", UserSchema);
module.exports = userTbl;
